  <aside class="notes">
      Changes are tricky enough when they’re done one at a time.
      When done two at a time, they can introduce subtle errors that look like the original errors.
      Then you’re in the awkward position of not knowing whether you didn’t correct the error,
      whether you corrected the error but introduced a new one that looks similar,
      or whether you didn’t correct the error and you introduced a similar new error.
      Keep it simple: make just one change at a time.
