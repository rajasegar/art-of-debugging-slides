  <h2>#8: Get a Fresh View</h2>
  <Image src="assets/looking-good-looking-fresh.gif" alt="Get a Fresh view" />
  <p>Take a break from the problem</p>
  <aside class="notes">
      Sometimes you concentrate so hard you can’t think.
      How many times have you paused for a cup of coffee and figured out the problem on your way to the coffee machine?
      Or in the middle of lunch?
      Or on the way home?
      Or in the shower the next morning?
      If you’re debugging and making no progress, once you’ve tried all the options, let it rest.
      Go for a walk. Work on something else. Go home for the day.
      Let your subconscious mind tease a solution out of the problem.
      The auxiliary benefit of giving up temporarily is that it reduces the anxiety associated with debugging.
      The onset of anxiety is a clear sign that it’s time to take a break.
  </aside>
